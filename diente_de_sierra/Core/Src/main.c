/**
  * @brief TIM10 Initialization Function
  * @param None
  * @retval None
  */
static void MX_TIM10_Init(void)
{

  /* USER CODE BEGIN TIM10_Init 0 */
	//Se obtiene la frecuencia del CLK que controla el TIM10
	pclk = HAL_RCC_GetPCLK2Freq();
	if ((RCC->CFGR & RCC_CFGR_PPRE2) != RCC_CFGR_PPRE2_DIV1) {
		timer_clk = pclk * 2;  // Si el divisor es diferente a 1, se duplica la frecuencia del reloj
	} else {
		timer_clk = pclk; //Si el divisor es 1, se mantiene igual
	}

	htim10.Init.Prescaler = 8399;
	//OJO: La siguiente línea de código permite modificar la frecuencia del timer al sólo cambiar freq_timer.
	//Note que la línea que configura el período está comentada en la sección de configuración del TIM10.
	//Si se genera nuevo código, debe comentar la línea en donde se asigna el valor del htim10.Init.Period en la
	//configuración del Timer.
	htim10.Init.Period = ((timer_clk / (htim10.Init.Prescaler + 1)) / freq_timer) - 1;

  /* USER CODE END TIM10_Init 0 */

  /* USER CODE BEGIN TIM10_Init 1 */

  /* USER CODE END TIM10_Init 1 */
  htim10.Instance = TIM10;
  htim10.Init.Prescaler = 8399;
  htim10.Init.CounterMode = TIM_COUNTERMODE_UP;
  //htim10.Init.Period = 65535;
  htim10.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
  htim10.Init.AutoReloadPreload = TIM_AUTORELOAD_PRELOAD_DISABLE;
  if (HAL_TIM_Base_Init(&htim10) != HAL_OK)
  {
    Error_Handler();
  }
  /* USER CODE BEGIN TIM10_Init 2 */

  /* USER CODE END TIM10_Init 2 */

}

